struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};


class Solution {
public:
    int sumOfLeftLeaves(TreeNode* root) {
        queue<TreeNode*> q;
        q.push(root);
        int sum=0;
        while (!q.empty())
        {
            int sz=q.size();
            for(int i=1; i<=sz; i++){
                TreeNode* f=q.front();
                q.pop();
                if(f->left && f->left->left==NULL && f->left->right==NULL){
                    sum+=f->left->val;
                }
                if(f->left!=NULL){
                    q.push(f->left);
                }
                if(f->right!=NULL){
                    q.push(f->right);
                }
            }
        }
        return sum;
        
    }
};